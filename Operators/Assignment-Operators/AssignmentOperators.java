public class AssignmentOperators 
{
    public static void main(String[] args) 
    {
        int number=100;
        System.out.println("Number:"+number);
        number +=10;
        System.out.println("AfterNumber:"+number);
        number -=10;
        System.out.println("AfterNumber:"+number);
        number *=10;
        System.out.println("AfterNumber:"+number);
        number /=10;
        System.out.println("AfterNumber:"+number);
        number %=10;
        System.out.println("AfterNumber:"+number);
 
    }
}
